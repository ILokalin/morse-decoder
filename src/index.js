const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
},
SPACE = '**********'

function decode(expr) {
    let wordResult = '';

    expr.match(/[01]{10}|[\*]{10}/g).forEach(char => {
        
        if (char === SPACE) {
            wordResult += ' '
        } else {
            wordResult += MORSE_TABLE[char.match(/[01]{2}/g).map(point => point === '11' ? '-' : point === '10' ? '.' : '').join('')];
        }
    });

    return wordResult;
}

module.exports = {
    decode
}